package nancy.com.foodchain.client;


public class Life {

	public Life(String name, int x, int y, int age, int volume, int width, int height, String icon) {
		super();
		this.name = name;
		this.x = x;
		this.y = y;
		this.age = age;
		this.volume = volume;
		this.width = width;
		this.height = height;
		this.icon = icon;
	}
	public String name;
	public int x;
	public int y;
	public int age = 0;
	public int volume = 1;
	public int width = 20;
	public int height = 20;
	public String icon;
	

}
